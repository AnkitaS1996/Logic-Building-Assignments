#include<stdio.h>
#include<conio.h>

void Pattern(iRow,iColumn) //////Pattern Function
{
    int i = 0, j = 0, No = 0;

    for(i = 1; i <= iRow; i++)
    {
        No = 5;
        for(j = 1; j <= iColumn; j++)
        {
            printf(" %d",No);
            No--;
        }
        printf("\n");
    }
}
int main()
{
            int iValue1 = 0, iValue2 = 0;
            printf("\n======================$$$$$$$$*******$$$$$$$$======================\n");

            printf("\n Enter Number Of Rows And Number Of Columns To Print Pattern : ");
            scanf("%d%d",&iValue1,&iValue2);
            printf("\n======================*****************************======================\n");

            Pattern(iValue1,iValue2);

            printf("\n=====================*****************************=======================\n");

            printf("\n Thank You..!!!! \n");
            printf("\n======================*****************************======================\n");

            getch();
            return 0;
}
