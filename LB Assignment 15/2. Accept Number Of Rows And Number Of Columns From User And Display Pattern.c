void Pattern(int iRow, int iColumn)
{
            int i = 0, j = 0,No = 0;
            //No = 1;
            for(i = 1; i <= iRow; i++)
            {
                        No = 1;
                        for(j = 1; j <= iColumn; j++)
                        {
                                printf(" %d",No);
                                No++;
                        }
                        printf("\n");
            }

            return;
}
