#include<stdio.h>
#include<conio.h>
void Pattern(int iNo)
{
            int i = 1;

            printf("\n");
            while(iNo >= i)
            {
                        printf("\t # ");
                        printf("\t %d",i);
                        printf("\t *");
                        i++;
            }
            printf("\n");
            return;
}
int main()
{
            int iValue = 0;
            printf("\n=================================*********================================\n");

            printf("\n Enter Number to Print Pattern : ");
            scanf("%d",&iValue);
            printf("\n=================================*********================================\n");

            Pattern(iValue);
            printf("\n=================================*********================================\n");

            printf("\n Thank You..!!!\n");
            printf("\n=================================*********================================\n");

            getch();
            return 0;
}
