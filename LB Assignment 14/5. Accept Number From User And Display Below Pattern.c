#include<stdio.h>
#include<conio.h>
void Pattern(int iNo)
{
            int i = 2, cnt = 0;

            printf("\n");

            for(cnt = 1; cnt <= iNo; cnt++)
            {
                    printf(" %d",i*cnt);
            }

            printf("\n");
}

int main()
{
            int iValue = 0;
            printf("\n=================================*********================================\n");
            printf("\n Enter Number to Print Pattern : ");
            scanf("%d",&iValue);

            printf("\n=================================*********================================\n");

            Pattern(iValue);

            printf("\n=================================*********================================\n");
            printf("\n Thank You..!!!!\n");
            printf("\n=================================*********================================\n");

            getch();
            return 0;

}
