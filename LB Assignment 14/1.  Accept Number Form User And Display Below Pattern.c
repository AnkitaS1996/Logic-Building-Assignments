#include<stdio.h>
#include<conio.h>
void Pattern(int iNo)
{
            int i = 0;
            char ch = 'A';

            printf("\n=========================*********===========================\n");
            printf("\n");
            for(i = 1;i <= iNo;i++)
            {
                    printf("\t %c",ch);
                    ch++;
            }
            printf("\n");
            printf("\n=========================*********===========================\n");

            return;
}
int main()
{
            int iValue = 0;
            printf("\n=========================*********===========================\n");

            printf("\n Enter Number Of Element : ");
            scanf("%d",&iValue);
            printf("\n=========================*********===========================\n");

            Pattern(iValue);

            printf("\n=========================*********===========================\n");

            printf("\n Thank You..!!!\n");
            printf("\n=========================*********===========================\n");

            getch();
            return 0;
}
